    private void pembayaranFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_pembayaranFocusLost
        // TODO add your handling code here:
            int TotalHarga = Integer.parseInt(totalharga.getText());
        int TotalBayar = Integer.parseInt(pembayaran.getText());
        int Kembali = TotalBayar - TotalHarga;
        if (Kembali < 0) {
            JOptionPane.showMessageDialog(null, "Uang Pembayaran Anda Kurang", 
                    "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            kembali.setText(String.valueOf(Kembali));
        }
    }//GEN-LAST:event_pembayaranFocusLost

