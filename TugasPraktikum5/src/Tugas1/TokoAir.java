    public void tampilData(){
         DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("ID Penjualan");
        tbl.addColumn("Nama Barang");
        tbl.addColumn("Kode Barang");
        tbl.addColumn("Harga Barang");
        tbl.addColumn("Nama Pembeli");
        tbl.addColumn("Jumlah Barang");
        tbl.addColumn("Harga Total");
        tbl.addColumn("Pembayaran");
        tbl.addColumn("Kembali");
        
        Tabel_Toko.setModel(tbl); 
        try {
            Statement st = conn.getConnection().createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM tabel_penjualan");
            while (rs.next()) {
                tbl.addRow( new Object[] {
                    rs.getString("id_penjualan"),
                    rs.getString("nama_barang"),
                    rs.getString("kode_barang"),
                    rs.getString("harga_barang"),
                    rs.getString("nama_pembeli"),
                    rs.getString("jumlah_barang"),
                    rs.getString("total_harga"),
                    rs.getString("total_pembayaran"),
                    rs.getString("kembali"),
                });
                Tabel_Toko.setModel(tbl);
            }
         } catch (Exception e) {
            System.out.println(e);
        }
    }
    
